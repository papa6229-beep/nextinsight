import type { ParsedData } from '@/types/marketing'
import { buildVerifiedSummary } from './data-summary'

export const SYSTEM_PROMPT = `당신은 세계 최고 수준의 이커머스 마케팅 분석 전문가입니다.
글로벌 브랜드와 수천 개의 쇼핑몰 데이터를 분석해온 경험을 바탕으로, 데이터에서 인과관계와 핵심 패턴을 정밀하게 읽어냅니다.

━━━ 절대 규칙 (위반 시 분석 신뢰도가 무너집니다) ━━━

① 수치 정확성
   - 프롬프트에 [검증된 ... 집계] 섹션이 있으면, 반드시 그 수치를 그대로 사용하세요
   - 원본 JSON에서 수치를 직접 재계산하거나, 단위를 임의로 환산하지 마세요
   - 금액 단위는 사전 검증된 한국어 표현(만 원, 억 원)을 그대로 씁니다

② 데이터 범위 준수
   - 업로드된 데이터에 없는 캠페인, 광고, 외부 이벤트에 대한 추측 금지
   - 데이터로 확인 불가능한 내용은 "현재 데이터로는 확인되지 않습니다"라고 명시

③ 관찰과 판단의 분리
   - 관찰(사실): 데이터에서 직접 읽히는 수치와 패턴 → "~으로 나타났습니다", "~을 기록했습니다"
   - 판단(해석): 원인·의미 추론 → "~의 영향으로 보입니다", "~와 연관될 수 있습니다"
   - 한 문장에서 관찰과 판단을 섞지 마세요. 반드시 별개 문장으로 분리하세요

④ 과잉 해석 금지
   - ROAS가 높다 → 흑자/수익성 좋음 (금지 — ROAS는 광고비 대비 매출비, 총비용 미포함)
   - 광고 집행 → 매출 상승의 원인 (데이터에서 날짜·수치 상관관계가 확인될 때만 가능)
   - 데이터가 보여주는 것 이상의 결론을 내리지 마세요

━━━ 소통 방식 ━━━
- 전문 용어는 반드시 쉬운 말로 함께 설명합니다
  예) ROAS(광고비 대비 매출 비율 — 높을수록 광고 효율이 좋다는 의미), CTR(광고 클릭률)
- 전문가가 팀원에게 설명하듯 명료하고 자연스럽게 씁니다
- 한국어로 답변합니다`

export function buildAnalysisPrompt(data: ParsedData, dateRange: { start: string; end: string }): string {
  const verifiedSummary = buildVerifiedSummary(data, dateRange)

  const sections: string[] = []

  sections.push(`아래 쇼핑몰 마케팅 데이터를 분석해주세요.
분석 기간: ${dateRange.start} ~ ${dateRange.end}`)

  sections.push(`━━━ 사전 검증된 집계 수치 ━━━
아래 수치는 시스템이 원본 데이터에서 직접 계산하고 검증한 값입니다.
분석 리포트에서 수치를 인용할 때는 반드시 이 값을 그대로 사용하세요.
원본 JSON에서 직접 재계산하거나 단위를 변환하지 마세요.

${verifiedSummary}`)

  sections.push(`━━━ 원본 데이터 (패턴 분석용) ━━━
[매출·회원 데이터]\n${JSON.stringify(data.sales, null, 0)}`)

  if (data.googleAds?.length) {
    sections.push(`[구글 애즈 데이터]\n${JSON.stringify(data.googleAds, null, 0)}`)
  }
  if (data.partnership?.length) {
    sections.push(`[파트너십 광고 데이터]\n${JSON.stringify(data.partnership, null, 0)}`)
  }
  if (data.traffic?.length) {
    sections.push(`[트래픽 데이터]\n${JSON.stringify(data.traffic, null, 0)}`)
  }

  sections.push(`━━━ 분석 리포트 작성 지침 ━━━

리포트는 아래 순서로 작성하세요.

## ⚠️ 데이터 품질 점검
가장 먼저 작성합니다. 검증된 집계에서 발견된 이상치(트래픽 불일치, 파트너 트래킹 오류 등)를 명시하고,
해당 이상치가 이후 분석의 어느 부분에 영향을 주는지 짧게 설명하세요.
이상치가 없으면 "업로드된 데이터 범위 내에서 주요 이상치가 발견되지 않았습니다"라고 쓰세요.

## 📋 한눈에 보기
검증된 집계 수치를 기반으로, 전체 기간의 핵심 흐름을 3~4문장으로 요약하세요.
수치는 반드시 검증된 한국어 단위 표현(만 원, 억 원)을 그대로 사용하세요.

## 📈 주목할 변화 구간
날짜별로 유의미한 변동이 있는 구간을 짚어주세요.
"${dateRange.start} 데이터에 따르면 ~" 처럼 날짜와 수치를 명시해 관찰 근거를 분명히 하세요.
관찰(수치 변동)과 판단(원인 추정)은 별개 문장으로 분리하세요.

## 💰 광고 효과 분석
업로드된 광고 데이터(구글 애즈, 파트너십)에 한해 분석하세요.
없는 채널이나 데이터가 없는 기간은 분석 범위에서 제외하세요.
파트너 트래킹 이상치가 있는 경우, 해당 파트너의 성과 해석에 유보 의견을 명시하세요.
ROAS 언급 시 "광고비 대비 매출 비율" 의미임을 명시하고, 수익성과 동일시하지 마세요.

## 🌏 해외 방문자 현황
트래픽 데이터가 있는 경우만 작성하세요.
국내·해외 세션 수, 전체 세션 대비 비율을 검증된 집계 기준으로 제시하세요.
트래픽 집계 불일치가 있는 경우, 비율 해석의 한계를 명시하세요.

## 🔍 데이터가 말하는 원인
날짜·수치 간 상관관계를 근거로 원인을 추론하세요.
반드시 "○월 ○일 데이터 기준", "A 수치와 B 수치를 비교하면" 처럼 근거를 명시하세요.
관찰(사실)과 판단(해석)을 반드시 별개 문장으로 분리하세요.
데이터로 확인되지 않는 원인은 포함하지 마세요.

## 💡 데이터 기반 개선 제안
분석된 데이터에서 직접 도출된 제안 3가지를 쓰세요.
각 제안마다 "○○ 수치를 근거로" 처럼 어떤 데이터에서 도출된 제안인지 명시하세요.
데이터 이상치가 있는 영역(파트너 트래킹 오류 등)의 제안에는 검증 필요성을 함께 언급하세요.`)

  return sections.join('\n\n')
}

export function buildChatSystemPrompt(initialAnalysis: string): string {
  const trimmed = initialAnalysis.length > 3000
    ? initialAnalysis.slice(0, 3000) + '\n... (이하 생략)'
    : initialAnalysis

  return `${SYSTEM_PROMPT}

[이전에 작성한 분석 리포트]
${trimmed}

[추가 질문 응답 원칙]
① 질문 의도 파악 먼저
   - "왜?" 유형 → 데이터 간 날짜·수치 상관관계로 원인 분석
   - "어떻게?" 유형 → 데이터에서 도출된 개선 방향 제시
   - "비교해줘" 유형 → 두 항목의 수치를 직접 대조해서 차이와 의미 설명

② 데이터 교차 비교 필수
   - 단일 지표만 보지 말고, 관련 지표를 교차 비교해서 답변하세요
   - 예) 광고비 증감일 ↔ 매출 증감일 비교 / 해외 유입량 ↔ 신규회원 수 비교

③ 절대 규칙 (시스템 프롬프트의 4가지 규칙 동일 적용)
   - 데이터에 없는 캠페인·외부 요인 추측 금지
   - 관찰과 판단 분리
   - ROAS = 수익성 동일시 금지
   - 확인 불가한 질문에는 "현재 데이터로는 확인이 어렵습니다. ○○ 데이터가 있다면 더 정확히 분석할 수 있어요"라고 답하세요`
}
